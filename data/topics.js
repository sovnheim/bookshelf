import { selectRandomItem, selectRandomProperty } from '../modules/utils.js';

export function getTopic() {
  const topic = selectRandomProperty(Topics);
  const subtopic = selectRandomItem(topic.subtopic);

  if (topic.associationSubtype != undefined) {
    const association = selectRandomProperty(
      Associations[topic.associationSubtype]
    );
    return subtopic + ' ' + association;
  } else {
    return subtopic;
  }
}

